import numpy as np
from sklearn.linear_model import LinearRegression

# ==========================
# Raw sequence (1960–2023)
# ==========================
years = np.array([
    1960,1961,1962,1963,1964,1965,1966,1967,1968,1969,
    1970,1971,1972,1973,1974,1975,1976,1977,1978,1979,
    1980,1981,1982,1983,1984,1985,1986,1987,1988,1989,
    1990,1991,1992,1993,1994,1995,1996,1997,1998,1999,
    2000,2001,2002,2003,2004,2005,2006,2007,2008,2009,
    2010,2011,2012,2013,2014,2015,2016,2017,2018,2019,
    2020,2021,2022,2023
])

values = np.array([
    30,45,92,62,15,28,82,16,30,3,
    94,54,89,33,31,19,51,52,50,74,
    44,4,96,15,17,30,40,94,59,7,
    37,50,25,91,75,39,8,65,72,6,
    9,83,33,20,62,3,40,61,66,90,
    41,25,70,67,29,63,93,6,19,15,
    66,19,0,94
])

# ==========================
# Train model
# ==========================
X = years.reshape(-1, 1)
y = values

model = LinearRegression()
model.fit(X, y)

# ==========================
# Predict 2024
# ==========================
year_to_predict = np.array([[2024]])
prediction = model.predict(year_to_predict)[0]

# Force result into 0–99 range
prediction_mod_100 = int(prediction) % 100

print("Predicción para 2024:", prediction_mod_100)
